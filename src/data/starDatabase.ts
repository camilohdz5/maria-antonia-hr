// Función para obtener el color basado en la temperatura
export const getStarColor = (temperature: number): string => {
  if (temperature >= 30000) return "#9bb0ff"; // O - Azul
  if (temperature >= 10000) return "#aabfff"; // B - Azul-blanco
  if (temperature >= 7500) return "#cad7ff"; // A - Blanco
  if (temperature >= 6000) return "#f8f7ff"; // F - Amarillo-blanco
  if (temperature >= 5200) return "#fff4ea"; // G - Amarillo
  if (temperature >= 3700) return "#ffcc6f"; // K - Naranja
  return "#ffcc6f"; // M - Rojo
};

// Función para obtener el tamaño del punto basado en la luminosidad
export const getStarSize = (luminosity: number): number => {
  // Escala logarítmica para el tamaño
  const size = 3 + Math.log10(luminosity + 1) * 2;
  return Math.max(2, Math.min(size, 15)); // Entre 2 y 15 píxeles
};
